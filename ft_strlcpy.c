#include <stddef.h>

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	ret;

	i = 0;
	ret = 0;
	while (src[ret])
		ret++;
	while (src[i] && i < size)
	{
		dst[i] = src[i];
		i++;
	}
	if (size > 0)
	{
		if (i == size)
			i--;
		dst[i] = '\0';
	}
	return (ret);
}
